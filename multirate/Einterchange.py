#plt.setp(markers42, 'color', 'r', 'markersize', 3)
#plt.setp(stemlines42, 'color', 'r', 'linewidth', 1)
ax4.annotate(r"$y[n] = expand\ x_a[n]\ with\ L$", xy=(0, 0), xytext=(-18, 3.5))

ax5.set_xlim(-20, 20)
ax5.set_ylim(0, 4)
#ax1.xaxis.set_ticks()
markers51, stemlines51, baseline51 = ax5.stem(x1, f(x1)*mod(x1))
plt.setp(markers51, 'markersize', 3, 'color', 'r')
plt.setp(stemlines51, 'color', 'r', 'linewidth', '1')
ax5.annotate(r"$x_b[n] = expand\ x[n]\ with\ L$", xy=(0, 0), xytext=(-18, 3.5), color='r')

ax6.set_xlim(-20, 20)
ax6.set_ylim(0, 4)
#ax1.xaxis.set_ticks()
markers63, stemlines63, baseline63 = ax6.stem(x1, f(x1)*mod(x1))
plt.setp(markers63, 'color', 'r', 'markersize', 3)
plt.setp(stemlines63, 'color', 'r', 'linewidth', 1)
markers61, stemlines61, baseline61 = ax6.stem(x1, h(x1)*mod(x1), markerfmt='x')
